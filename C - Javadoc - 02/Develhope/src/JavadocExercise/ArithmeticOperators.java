package JavadocExercise;

/**
 * Exercise to understanding Javadoc.
 * Using four methods with arithmetic operators.
 * @author Alessandro Trupia
 * @version 1.0
 */
public class ArithmeticOperators {

    /**
     *
     * @param num1
     * @param num2
     * @return the summation  of num1 and num2
     */
    public int sum(int num1, int num2){
        return num1 + num2;
    }

    /**
     *
     * @param num1
     * @param num2
     * @return the subtraction  of num1 and num2
     */
    public int sub(int num1, int num2){
        return num1 - num2;
    }

    /**
     *
     * @param num1
     * @param num2
     * @return the multiplication  of num1 and num2
     */
    public int mul(int num1, int num2){
        return num1 * num2;
    }

    /**
     *
     * @param num1
     * @param num2
     * @return the division  of num1 and num2
     */
    public int div(int num1, int num2){
        return num1 / num2;
    }

}
